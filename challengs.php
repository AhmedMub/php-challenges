//*Solution:
function elzero3($arr)
{
    $keys = [];
    $vals = [];
    foreach ($arr as $key => $val) {
        $keys[] = strtolower($val);
        $vals[] = $key;
    }
    $results = array_combine($keys, $vals);
    return array_reverse($results, true);
}
